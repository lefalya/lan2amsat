class picture : 

    def __init__():
        self.name = ""
        self.relative_path = "" 
        self.date = "" 

    def set_name(name) :
        self.name = name 

    def set_relative_path(relative_path):
        self.relative_path = relative_path

    def set_date(date):
        self.date = date 

    def get_name() :
        return self.name 

    def get_relative_path():
        return self.relative_path 

    def get_date():
        return self.date 
